    public void LoadData(){
        if(dsDichVu != null)
            for (int i = 0; i < dsDichVu.size(); i++) {
                model.removeRow(0);
            }
        try {
            dsDichVu = DICHVU.getAll();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không lấy danh sách dịch vụ!");
            return;
        }
        for (int i = 0; i < dsDichVu.size(); i++) {
            Vector vt = new Vector();
            vt.add(i + 1);
            vt.add(dsDichVu.get(i).getMaDichVu());
            vt.add(dsDichVu.get(i).getTenDichVu());
            vt.add(dsDichVu.get(i).getDonGia());
            model.addRow(vt);
        }
    }
