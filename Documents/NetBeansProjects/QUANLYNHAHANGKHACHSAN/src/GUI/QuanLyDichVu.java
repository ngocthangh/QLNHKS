    private void btnThemDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDVActionPerformed
        String MaDichVu = "", TenDichVu = "";
        double DonGia;
        MaDichVu = txtMaDichVu.getText();
        TenDichVu = txtTenDichVu.getText();
        if(TenDichVu.equals("") || txtDonGia.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin!");
            return;
        }
        try{
            DonGia = Double.parseDouble(txtDonGia.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Đơn giá phải là số, vui lòng nhập lại!");
            return;
        }
        if(DonGia < 0){
            JOptionPane.showMessageDialog(null,"Đơn giá phải lớn hơn 0, vui lòng nhập lại!");
            return;
        }
        
        DTO_DichVu monAn = new DTO_DichVu(MaDichVu, TenDichVu, DonGia);
        try {
            DICHVU.InsertNew(monAn);
            LoadData();
            JOptionPane.showMessageDialog(null,"Thêm dịch vụ thành công!");
            rowSelected = -1;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể thêm dịch vụ \nMã dịch vụ đã tồn tại!");
            return;
        }
    }//GEN-LAST:event_btnThemDVActionPerformed

    private void btnSuaDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDVActionPerformed
         String MaDichVu = "", TenDichVu = "";
        double DonGia;
        MaDichVu = txtMaDichVu.getText();
        TenDichVu = txtTenDichVu.getText();
        try{
            DTO_DichVu dichvu = DICHVU.getById(MaDichVu);
            if(dichvu == null){
                JOptionPane.showMessageDialog(null,"Mã dịch vụ không tồn tại!\nVui lòng chọn 1 dịch vụ từ danh sách");
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể kết nối!");
            return;
        }
        if(TenDichVu.equals("") || txtDonGia.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin!");
            return;
        }
        try{
            DonGia = Double.parseDouble(txtDonGia.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Đơn giá phải là số, vui lòng nhập lại!");
            return;
        }
        if(DonGia < 0){
            JOptionPane.showMessageDialog(null,"Đơn giá phải lớn hơn 0, vui lòng nhập lại!");
            return;
        }
        
        DTO_DichVu monAn = new DTO_DichVu(MaDichVu, TenDichVu, DonGia);
        try {
            DICHVU.Update(monAn);
            LoadData();
            JOptionPane.showMessageDialog(null,"Cập nhật dịch vụ thành công!");
            rowSelected = -1;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể cập nhật dịch vụ!");
            return;
        }
    }//GEN-LAST:event_btnSuaDVActionPerformed

