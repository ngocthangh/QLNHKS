    private void btnThemKhachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKhachActionPerformed
        String MaKhachHang = "", TenKhachHang = "", CMND = "", DiaChi = "", Email = "", SDT = "", QuocTich = "";
        MaKhachHang = txtMaKhachHang.getText();
        TenKhachHang = txtTenKhachHang.getText();
        DiaChi = txtDiaChi.getText();
        Email = txtEmail.getText();
        CMND = txtCMND.getText();
        SDT = txtSDT.getText();
        QuocTich = cbxQuocTich.getSelectedItem().toString();
        if(TenKhachHang.equals("") || DiaChi.equals("") || Email.equals("") || SDT.equals("") || QuocTich.equals("")){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin!");
            return;
        }
        DTO_KhachHang khachHang = new DTO_KhachHang(MaKhachHang, TenKhachHang, CMND, DiaChi, Email, SDT, QuocTich);
        try {
            KHACHHANG.InsertNew(khachHang);
            LoadData();
            JOptionPane.showMessageDialog(null,"Thêm khách hàng thành công!");
            rowSelected = -1;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể thêm khách hàng \nMã khách hàng đã tồn tại! " + ex);
            return;
        }
    }//GEN-LAST:event_btnThemKhachActionPerformed

