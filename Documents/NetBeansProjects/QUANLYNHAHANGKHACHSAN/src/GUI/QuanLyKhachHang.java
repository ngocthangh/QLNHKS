    public void LoadData(){
        if(dsKhachHang != null)
            for (int i = 0; i < dsKhachHang.size(); i++) {
                model.removeRow(0);
            }
        try {
            dsKhachHang = KHACHHANG.getAll();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể lấy danh sách khách hàng!" + ex);
            return;
        }
        for (int i = 0; i < dsKhachHang.size(); i++) {
            Vector vt = new Vector();
            vt.add(dsKhachHang.get(i).getMaKhachHang());
            vt.add(dsKhachHang.get(i).getTenKhachHang());
            vt.add(dsKhachHang.get(i).getCMND());
            vt.add(dsKhachHang.get(i).getDiaChi());
            vt.add(dsKhachHang.get(i).getEmail());
            vt.add(dsKhachHang.get(i).getSoDienThoai());
            vt.add(dsKhachHang.get(i).getQuocTich());
            model.addRow(vt);
        }
    }
