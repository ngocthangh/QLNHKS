    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String MaKhachHang = "", TenKhachHang = "";
        double DonGia;
        if(rowSelected < 0){
            JOptionPane.showMessageDialog(null,"Vui lòng chọn khách hàng cần xóa!");
            return;
        }
        MaKhachHang = tableKhachHang.getValueAt(rowSelected, 0).toString();
        TenKhachHang = tableKhachHang.getValueAt(rowSelected, 1).toString();
        if(JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa khách hàng " + TenKhachHang, "Xác nhận", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            try {
                KHACHHANG.Delete(MaKhachHang);
                LoadData();
                JOptionPane.showMessageDialog(null,"Xóa khách hàng thành công!");
                rowSelected = -1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Không thể xóa khách hàng!" + ex);
                return;
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

