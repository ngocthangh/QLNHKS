    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String MaLoaiPhong = "", TenLoaiPhong = "";
        double DonGia;
        if(rowSelected < 0){
            JOptionPane.showMessageDialog(null,"Vui lòng chọn loại phòng cần xóa!");
            return;
        }
        MaLoaiPhong = tableLoaiPhong.getValueAt(rowSelected, 1).toString();
        TenLoaiPhong = tableLoaiPhong.getValueAt(rowSelected, 2).toString();
        if(JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa loại phòng " + TenLoaiPhong, "Xác nhận", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            try {
                LOAIPHONG.Delete(MaLoaiPhong);
                LoadData();
                JOptionPane.showMessageDialog(null,"Xóa loại phòng thành công!");
                rowSelected = -1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Không thể xóa loại phòng!");
                return;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

