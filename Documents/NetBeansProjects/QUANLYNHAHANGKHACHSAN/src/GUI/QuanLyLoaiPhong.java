    public void LoadData(){
        if(dsLoaiPhong != null)
            for (int i = 0; i < dsLoaiPhong.size(); i++) {
                model.removeRow(0);
            }
        try {
            dsLoaiPhong = LOAIPHONG.getAll();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không lấy danh sách loại phòng!");
            return;
        }
        for (int i = 0; i < dsLoaiPhong.size(); i++) {
            Vector vt = new Vector();
            vt.add(i + 1);
            vt.add(dsLoaiPhong.get(i).getMaLoaiPhong());
            vt.add(dsLoaiPhong.get(i).getTenLoaiPhong());
            vt.add(dsLoaiPhong.get(i).getDonGia());
            vt.add(dsLoaiPhong.get(i).getMoTa());
            model.addRow(vt);
        }
    }
