    private void btnThemLoaiPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLoaiPhongActionPerformed
        String MaLoaiPhong = "", TenLoaiPhong = "", MoTa = "";
        double DonGia;
        MaLoaiPhong = txtMaLoaiPhong.getText();
        TenLoaiPhong = txtTenLoaiPhong.getText();
        MoTa = txtMoTa.getText();
        if(TenLoaiPhong.equals("") || txtDonGia.getText().equals("") || MoTa.equals("")){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin!");
            return;
        }
        try{
            DonGia = Double.parseDouble(txtDonGia.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Đơn giá phải là số, vui lòng nhập lại!");
            return;
        }
        if(DonGia < 0){
            JOptionPane.showMessageDialog(null,"Đơn giá phải lớn hơn 0, vui lòng nhập lại!");
            return;
        }

        DTO_LoaiPhong loaiPhong = new DTO_LoaiPhong(MaLoaiPhong, TenLoaiPhong, DonGia, MoTa);
        try {
            LOAIPHONG.InsertNew(loaiPhong);
            LoadData();
            JOptionPane.showMessageDialog(null,"Thêm loại phòng thành công!");
            rowSelected = -1;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể thêm loại phòng \nMã loại phòng đã tồn tại!");
            return;
        }
    }//GEN-LAST:event_btnThemLoaiPhongActionPerformed

    private void btnSuaLoaiPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaLoaiPhongActionPerformed
        String MaLoaiPhong = "", TenLoaiPhong = "", MoTa = "";
        double DonGia;
        MaLoaiPhong = txtMaLoaiPhong.getText();
        try{
            DTO_LoaiPhong loaiphong = LOAIPHONG.getById(MaLoaiPhong);
            if(loaiphong == null){
                JOptionPane.showMessageDialog(null,"Mã loại phòng không tồn tại!\nVui lòng chọn 1 loại phòng từ danh sách loại phòng");
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể kết nối!");
            return;
        }
        TenLoaiPhong = txtTenLoaiPhong.getText();
        MoTa = txtMoTa.getText();
        if(TenLoaiPhong.equals("") || txtDonGia.getText().equals("") || MoTa.equals("")){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin!");
            return;
        }
        try{
            DonGia = Double.parseDouble(txtDonGia.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Đơn giá phải là số, vui lòng nhập lại!");
            return;
        }
        if(DonGia < 0){
            JOptionPane.showMessageDialog(null,"Đơn giá phải lớn hơn 0, vui lòng nhập lại!");
            return;
        }

        DTO_LoaiPhong loaiPhong = new DTO_LoaiPhong(MaLoaiPhong, TenLoaiPhong, DonGia, MoTa);
        try {
            LOAIPHONG.Update(loaiPhong);
            LoadData();
            JOptionPane.showMessageDialog(null,"Cập nhật loại phòng thành công!");
            rowSelected = -1;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Không thể cập nhật loại phòng!");
            return;
        }
    }//GEN-LAST:event_btnSuaLoaiPhongActionPerformed

